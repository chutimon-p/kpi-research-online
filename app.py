conn = st.connection("gsheets", type=GSheetsConnection)

@st.cache_data(ttl=0)
def load_data():
    try:
        # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÅ‡∏¢‡∏Å‡πÅ‡∏ú‡πà‡∏ô‡∏á‡∏≤‡∏ô
        df_m = conn.read(worksheet="masters")
        df_r = conn.read(worksheet="research")
        
        # ‡∏•‡πâ‡∏≤‡∏á‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á
        df_m.columns = df_m.columns.str.strip()
        df_r.columns = df_r.columns.str.strip()
        
        return df_m, df_r
    except Exception as e:
        st.error(f"‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ: {e}")
        st.info("üí° ‡∏Ñ‡∏≥‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡πÄ‡∏õ‡∏¥‡∏î‡πÅ‡∏ä‡∏£‡πå‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏õ‡πá‡∏ô 'Anyone with the link' ‡∏´‡∏£‡∏∑‡∏≠‡∏¢‡∏±‡∏á? ‡πÅ‡∏•‡∏∞‡∏ä‡∏∑‡πà‡∏≠ Tab ‡∏ï‡πâ‡∏≠‡∏á‡πÄ‡∏õ‡πá‡∏ô 'masters' ‡πÅ‡∏•‡∏∞ 'research'")
        st.stop()

df_master, df_research = load_data()

# --- 3. ‡∏™‡πà‡∏ß‡∏ô Side Bar (‡πÄ‡∏°‡∏ô‡∏π‡πÅ‡∏•‡∏∞‡∏ï‡∏±‡∏ß‡∏Å‡∏£‡∏≠‡∏á) ---
with st.sidebar:
    st.title("üìå ‡∏£‡∏∞‡∏ö‡∏ö‡∏ö‡∏£‡∏¥‡∏´‡∏≤‡∏£‡∏á‡∏≤‡∏ô‡∏ß‡∏¥‡∏à‡∏±‡∏¢")
    menu = st.radio("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏´‡∏ô‡πâ‡∏≤‡∏à‡∏≠", ["üìä ‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô KPI", "‚úçÔ∏è ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏á‡∏≤‡∏ô"])
    st.divider()
    
    # ‡∏ï‡∏±‡∏ß‡∏Å‡∏£‡∏≠‡∏á‡∏õ‡∏µ ‡∏û.‡∏®.
    if not df_research.empty:
        all_years = sorted(df_research["‡∏õ‡∏µ"].dropna().unique().astype(int).tolist())
        year_sel = st.selectbox("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏õ‡∏µ ‡∏û.‡∏®.", ["‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î"] + [str(y) for y in all_years])
    else:
        year_sel = "‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î"

# --- 4. ‡∏´‡∏ô‡πâ‡∏≤‡∏ó‡∏µ‡πà 1: ‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô KPI ---
if menu == "üìä ‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô KPI":
    st.title(f"üìä ‡∏™‡∏£‡∏∏‡∏õ‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏î‡∏≥‡πÄ‡∏ô‡∏¥‡∏ô‡∏á‡∏≤‡∏ô ‡∏õ‡∏µ {year_sel}")
    
    # ‡∏Å‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
    df_f = df_research.copy()
    if year_sel != "‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î":
        df_f = df_f[df_f["‡∏õ‡∏µ"] == int(year_sel)]

    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏£‡∏≤‡∏¢‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£ (‡∏≠‡πâ‡∏≤‡∏á‡∏≠‡∏¥‡∏á‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏à‡∏≤‡∏Å‡∏´‡∏ô‡πâ‡∏≤ masters)
    progs = df_master[df_master["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"].notna() & (df_master["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"] != "-")]["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"].unique()
    report = pd.DataFrame(progs, columns=["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"])
    
    # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ‡∏Ñ‡∏ì‡∏∞ ‡πÅ‡∏•‡∏∞ ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏≠‡∏≤‡∏à‡∏≤‡∏£‡∏¢‡πå
    fac_map = df_master.drop_duplicates("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£").set_index("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")["‡∏Ñ‡∏ì‡∏∞"].to_dict()
    staff_cnt = df_master.groupby("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")["Name-surname"].nunique().to_dict()
    
    # ‡∏£‡∏ß‡∏°‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏à‡∏≤‡∏Å‡∏´‡∏ô‡πâ‡∏≤ research
    merged = df_f.merge(df_master[['Name-surname', '‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£']], left_on="‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô", right_on="Name-surname", how="left")
    score_sum = merged.groupby("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")["‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô"].sum().reset_index()
    
    report = report.merge(score_sum, on="‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£", how="left").fillna(0)
    report["‡∏Ñ‡∏ì‡∏∞"] = report["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"].map(fac_map)

    # ‡∏™‡∏π‡∏ï‡∏£‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì KPI Score (PhD=60, Master/Dip=40, ‡∏≠‡∏∑‡πà‡∏ô‡πÜ=20)
    def calc_kpi(row):
        p = row["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"]
        n = staff_cnt.get(p, 1)
        group_40 = ["G-Dip TH", "G-Dip Inter", "M. Ed-Admin", "M. Ed-LMS", "MBA", "MPH"]
        x_val = 60 if p == "Ph.D-Admin" else (40 if p in group_40 else 20)
        score = (((row["‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô"] / n) * 100) / x_val) * 5
        return round(min(score, 5.0), 2)

    report["‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô KPI"] = report.apply(calc_kpi, axis=1)
    report = report.sort_values("‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô KPI", ascending=False)

    # ‡πÅ‡∏™‡∏î‡∏á‡∏Å‡∏£‡∏≤‡∏ü
    fig = px.bar(report, x="‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô KPI", y="‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£", color="‡∏Ñ‡∏ì‡∏∞", orientation='h', height=750,
                 text="‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô KPI", color_discrete_sequence=px.colors.qualitative.Set3)
    fig.add_vline(x=5.0, line_dash="dash", line_color="red")
    st.plotly_chart(fig, use_container_width=True)
    
    st.write("### üìã ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏™‡∏£‡∏∏‡∏õ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏£‡∏≤‡∏¢‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")
    st.dataframe(report, use_container_width=True)

# --- 5. ‡∏´‡∏ô‡πâ‡∏≤‡∏ó‡∏µ‡πà 2: ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏á‡∏≤‡∏ô ---
else:
    st.title("‚úçÔ∏è ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏á‡∏≤‡∏ô‡πÉ‡∏´‡∏°‡πà")
    st.info("‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏õ‡∏¢‡∏±‡∏á‡πÅ‡∏ú‡πà‡∏ô‡∏á‡∏≤‡∏ô 'research' ‡πÉ‡∏ô Google Sheets")
    
    with st.form("research_form", clear_on_submit=True):
        t = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏á‡∏≤‡∏ô‡∏ß‡∏¥‡∏à‡∏±‡∏¢")
        col1, col2 = st.columns(2)
        with col1:
            y = st.number_input("‡∏õ‡∏µ ‡∏û.‡∏®.", 2560, 2600, 2568)
        with col2:
            s_map = {"TCI 1": 0.8, "TCI 2": 0.6, "Scopus Q1": 1.0, "Scopus Q2": 1.0, "Scopus Q3": 1.0, "Scopus Q4": 1.0}
            base = st.selectbox("‡∏ê‡∏≤‡∏ô‡∏ß‡∏≤‡∏£‡∏™‡∏≤‡∏£", list(s_map.keys()))
            
        authors = st.multiselect("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏≠‡∏≤‡∏à‡∏≤‡∏£‡∏¢‡πå‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô", sorted(df_master["Name-surname"].unique()))
        ext = st.text_input("‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô‡∏†‡∏≤‡∏¢‡∏ô‡∏≠‡∏Å (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ)")

        if st.form_submit_button("üíæ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•"):
            if t and authors:
                new_data = [{"‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á": t, "‡∏õ‡∏µ": y, "‡∏ê‡∏≤‡∏ô‡∏ß‡∏≤‡∏£‡∏™‡∏≤‡∏£": base, "‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô": s_map[base], "‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô": a, "‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô‡∏†‡∏≤‡∏¢‡∏ô‡∏≠‡∏Å": ext} for a in authors]
                updated_df = pd.concat([df_research, pd.DataFrame(new_data)], ignore_index=True)
                
                # ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏¢‡∏±‡∏á Google Sheets
                conn.update(worksheet="research", data=updated_df)
                
                st.success("‚úÖ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à!")
                st.cache_data.clear()
                st.rerun()
            else:
                st.warning("‚ö†Ô∏è ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á‡πÅ‡∏•‡∏∞‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô")
