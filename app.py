conn = st.connection("gsheets", type=GSheetsConnection)

@st.cache_data(ttl=0)
def load_data():
    try:
        # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å Tab ‡∏ä‡∏∑‡πà‡∏≠ 'masters' ‡πÅ‡∏•‡∏∞ 'research'
        df_m = conn.read(worksheet="masters")
        df_r = conn.read(worksheet="research")
        
        # ‡∏•‡πâ‡∏≤‡∏á‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡πÉ‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå
        df_m.columns = df_m.columns.str.strip()
        df_r.columns = df_r.columns.str.strip()
        
        return df_m, df_r
    except Exception as e:
        st.error(f"‚ùå ‡∏£‡∏∞‡∏ö‡∏ö‡∏´‡∏≤‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏°‡πà‡∏û‡∏ö ‡∏´‡∏£‡∏∑‡∏≠‡∏ä‡∏∑‡πà‡∏≠ Tab ‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á")
        st.info("‡∏Ñ‡∏≥‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡πÉ‡∏ô Google Sheets ‡∏ï‡∏±‡πâ‡∏á‡∏ä‡∏∑‡πà‡∏≠‡πÅ‡∏ú‡πà‡∏ô‡∏á‡∏≤‡∏ô‡∏ß‡πà‡∏≤ 'masters' ‡πÅ‡∏•‡∏∞ 'research' ‡∏´‡∏£‡∏∑‡∏≠‡∏¢‡∏±‡∏á?")
        st.stop()

df_master, df_research = load_data()

# ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ä‡∏∑‡πà‡∏≠‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏ï‡∏≤‡∏°‡πÑ‡∏ü‡∏•‡πå‡∏à‡∏£‡∏¥‡∏á‡∏ó‡∏µ‡πà‡∏Ñ‡∏∏‡∏ì‡∏™‡πà‡∏á‡∏°‡∏≤
if 'Name-surname' not in df_master.columns:
    st.error(f"‚ùå ‡∏´‡∏≤‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå 'Name-surname' ‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠ (‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏ö: {list(df_master.columns)})")
    st.stop()

# --- 3. ‡∏™‡πà‡∏ß‡∏ô‡∏Ñ‡∏ß‡∏ö‡∏Ñ‡∏∏‡∏° Side Bar ---
with st.sidebar:
    st.title("üìå ‡πÄ‡∏°‡∏ô‡∏π‡∏£‡∏∞‡∏ö‡∏ö")
    menu = st.radio("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏´‡∏ô‡πâ‡∏≤‡∏à‡∏≠", ["üìä ‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô KPI", "‚úçÔ∏è ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏á‡∏≤‡∏ô"])
    
    st.divider()
    all_years = sorted(df_research["‡∏õ‡∏µ"].dropna().unique().astype(int).tolist()) if not df_research.empty else []
    year_option = st.selectbox("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏õ‡∏µ ‡∏û.‡∏®.", ["‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î"] + [str(y) for y in all_years])

# ‡∏Å‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
df_filtered = df_research.copy()
if year_option != "‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î":
    df_filtered = df_filtered[df_filtered["‡∏õ‡∏µ"] == int(year_option)]

# --- 4. ‡∏´‡∏ô‡πâ‡∏≤‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏• KPI (21 ‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£) ---
if menu == "üìä ‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô KPI":
    st.title(f"üìä ‡∏™‡∏£‡∏∏‡∏õ‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏î‡∏≥‡πÄ‡∏ô‡∏¥‡∏ô‡∏á‡∏≤‡∏ô ‡∏õ‡∏µ {year_option}")
    
    # ‡∏î‡∏∂‡∏á‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏à‡∏≤‡∏Å‡∏´‡∏ô‡πâ‡∏≤ masters
    all_progs = df_master[df_master["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"].notna() & (df_master["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"] != "-")]["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"].unique()
    prog_df = pd.DataFrame(all_progs, columns=["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"])
    
    # ‡πÅ‡∏°‡∏õ ‡∏Ñ‡∏ì‡∏∞
    fac_map = df_master.drop_duplicates("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£").set_index("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")["‡∏Ñ‡∏ì‡∏∞"].to_dict()
    prog_df["‡∏Ñ‡∏ì‡∏∞"] = prog_df["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"].map(fac_map)
    
    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô
    df_merged = df_filtered.merge(df_master[['Name-surname', '‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£']], left_on="‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô", right_on="Name-surname", how="left")
    res_sum = df_merged.groupby("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")["‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô"].sum().reset_index()
    prog_df = prog_df.merge(res_sum, on="‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£", how="left").fillna(0)
    
    # ‡∏™‡∏π‡∏ï‡∏£ KPI
    staff_counts = df_master.groupby("‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£")["Name-surname"].nunique().to_dict()
    def calc_kpi(row):
        p = row["‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£"]
        n = staff_counts.get(p, 1)
        x = 60 if p == "Ph.D-Admin" else (40 if p in ["G-Dip TH", "G-Dip Inter", "M. Ed-Admin", "M. Ed-LMS", "MBA", "MPH"] else 20)
        score = (((row["‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô"] / n) * 100) / x) * 5
        return round(min(score, 5.0), 2)

    prog_df["‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô KPI"] = prog_df.apply(calc_kpi, axis=1)
    
    # ‡∏Å‡∏£‡∏≤‡∏ü
    fig = px.bar(prog_df, x="‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô KPI", y="‡∏´‡∏•‡∏±‡∏Å‡∏™‡∏π‡∏ï‡∏£", orientation='h', color="‡∏Ñ‡∏ì‡∏∞", height=800)
    fig.add_vline(x=5.0, line_dash="dash", line_color="red")
    st.plotly_chart(fig, use_container_width=True)
    st.dataframe(prog_df, use_container_width=True)

# --- 5. ‡∏´‡∏ô‡πâ‡∏≤‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏á‡∏≤‡∏ô ---
else:
    st.title("‚úçÔ∏è ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏á‡∏≤‡∏ô‡πÉ‡∏´‡∏°‡πà")
    with st.form("add_form", clear_on_submit=True):
        t = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á")
        y = st.number_input("‡∏õ‡∏µ ‡∏û.‡∏®.", 2567, 2600, 2568)
        b = st.selectbox("‡∏ê‡∏≤‡∏ô‡∏ß‡∏≤‡∏£‡∏™‡∏≤‡∏£", ["TCI1", "TCI2", "Scopus Q1", "Scopus Q2", "Scopus Q3", "Scopus Q4"])
        a = st.multiselect("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô", df_master["Name-surname"].dropna().unique())
        
        if st.form_submit_button("üíæ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å"):
            if t and a:
                scores = {"TCI1": 0.8, "TCI2": 0.6, "Scopus Q1": 1.0, "Scopus Q2": 1.0, "Scopus Q3": 1.0, "Scopus Q4": 1.0}
                new_data = pd.DataFrame([{"‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á": t, "‡∏õ‡∏µ": y, "‡∏ê‡∏≤‡∏ô‡∏ß‡∏≤‡∏£‡∏™‡∏≤‡∏£": b, "‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô": scores[b], "‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô": i} for i in a])
                conn.update(worksheet="research", data=pd.concat([df_research, new_data], ignore_index=True))
                st.success("‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à!")
                st.cache_data.clear()
                st.rerun()
